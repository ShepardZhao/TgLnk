- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    // Return the number of rows in the section.
    return 2;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"aboutReuseIdentifier" forIndexPath:indexPath];
    
    if (indexPath.section == 0 && indexPath.row==0) {
        cell.textLabel.text = @"Term of Services";
    }
    else if(indexPath.section == 0 && indexPath.row ==1){
    
        cell.textLabel.text = @"Privacy Policy";

    
    }
    return cell;
}

-(void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath{

    if (indexPath.section ==0 && indexPath.row==0) {
        [self performSegueWithIdentifier:@"privacyPolcySegue" sender:self];
    }
    else if (indexPath.section == 0 && indexPath.row ==1){
        [self performSegueWithIdentifier:@"termsOfServiceSegue" sender:self];
    }


}


